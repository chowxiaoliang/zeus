package com.zl.person;

public class SameOne {

    boolean ifSamePerson;

    String name;

    String certNo;

    String mobile;

    public boolean isIfSamePerson() {
        return ifSamePerson;
    }

    public void setIfSamePerson(boolean ifSamePerson) {
        this.ifSamePerson = ifSamePerson;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCertNo() {
        return certNo;
    }

    public void setCertNo(String certNo) {
        this.certNo = certNo;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }
}
